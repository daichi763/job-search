export function occupationCatalogText() {
  const byTab = {}
  for (const [code, e] of Object.entries(OCCUPATIONS)) {
    (byTab[e.tab] = byTab[e.tab] || []).push(`${code}:${e.label}`)
  }
  return Object.entries(byTab).map(([t, arr]) => `【${t}】 ${arr.join(' / ')}`).join('\n')
}
