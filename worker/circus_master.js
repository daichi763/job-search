// ---------------------------------------------------------------------------
// 逆引きヘルパー: AIプランが人間可読ラベル/キーワードでも、コード番号でも
// 指定できるようにする。resolveXxx(input) は string / number / 配列 を受けて
// 有効なコード文字列の配列を返す。
// ---------------------------------------------------------------------------

// 全角/半角・記号ゆらぎを吸収して照合用に正規化
function norm(s) {
  return String(s)
    .toLowerCase()
    .replace(/[\s　]/g, '')
    .replace(/[（）()]/g, '')
    .replace(/[・･、,／/]/g, '')
    .replace(/[〜~]/g, '')
    .trim()
}

// {code:{label,tab}} または {code:label} の両対応でラベル文字列を取得
function labelOf(entry) {
  return (entry && typeof entry === 'object') ? entry.label : entry
}

// 単一マップに対する汎用リゾルバを生成
function makeResolver(map) {
  // 事前に正規化ラベル→code の索引を構築
  const byNormLabel = {}
  for (const [code, entry] of Object.entries(map)) {
    const lbl = labelOf(entry)
    if (lbl) byNormLabel[norm(lbl)] = code
  }
  return function resolve(input) {
    if (input == null) return []
    const arr = Array.isArray(input) ? input : [input]
    const out = new Set()
    for (const raw of arr) {
      if (raw == null || raw === '') continue
      const s = String(raw).trim()
      // 1) そのままコード番号として存在するか
      if (Object.prototype.hasOwnProperty.call(map, s)) { out.add(s); continue }
      const n = norm(s)
      // 2) 完全一致(正規化)
      if (byNormLabel[n]) { out.add(byNormLabel[n]); continue }
      // 3) 部分一致 (入力がラベルの一部 / ラベルが入力の一部)
      let hit = null
      for (const [code, entry] of Object.entries(map)) {
        const ln = norm(labelOf(entry))
        if (!ln) continue
        if (ln === n) { hit = code; break }
        if (ln.includes(n) || n.includes(ln)) { hit = hit || code }
      }
      if (hit) out.add(hit)
    }
    return [...out]
  }
}

